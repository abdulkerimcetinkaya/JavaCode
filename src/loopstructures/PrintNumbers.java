package loopstructures;

public class PrintNumbers {
    public static void main(String[] args) {

        System.out.println("For Loops");
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println("\nWhile Loops");
        int i = 0;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }


    }
}
